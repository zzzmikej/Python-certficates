# Aula de operadores Matematicas

num1 = 1
num2 = 1

print('A soma dos valores é:', num1 + num2)
print('A subtração dos valores é:', num1 - num2)
print('A divisão dos valores é:', num1 / num2)
print('A divisão inteira dos valores é:', num1 // num2)
print('O resto dos valores é:', num1 % num2)
print('A potencia dos valores é:', num1 ** num2)

print(num1 > num2)
print(num1 <= num2)
print(num1 == num2)
print(num1 != num2)
print(num1 >= num2)
